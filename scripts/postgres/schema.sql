CREATE TABLE IF NOT EXISTS ewc_news_posts (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  -- Owned by EITHER a game (game_slug) OR a media channel (media_slug).
  game_slug         TEXT,
  media_slug        TEXT,
  locale            TEXT NOT NULL DEFAULT 'en' CHECK (locale IN ('en','ar')),
  content_mode      TEXT NOT NULL DEFAULT 'shared' CHECK (content_mode IN ('shared','translated')),
  default_locale    TEXT NOT NULL DEFAULT 'en' CHECK (default_locale IN ('en','ar')),
  title             TEXT NOT NULL,
  summary           TEXT NOT NULL DEFAULT '',
  body              TEXT NOT NULL DEFAULT '',
  status            TEXT NOT NULL DEFAULT 'draft' CHECK (status IN ('draft','published')),
  author_discord_id TEXT,
  author_name       TEXT,
  cover_image_url   TEXT,
  cover_placement   TEXT,
  ewc               INTEGER NOT NULL DEFAULT 0,
  created_at        TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  updated_at        TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  published_at      TEXT
);

-- Migration: add the EWC tag to pre-existing installs (idempotent; runs on boot).
ALTER TABLE ewc_news_posts ADD COLUMN IF NOT EXISTS ewc INTEGER NOT NULL DEFAULT 0;
-- Migration: media-channel ownership. game_slug becomes nullable so media posts can
-- omit it; media_slug names the owning channel when set.
ALTER TABLE ewc_news_posts ADD COLUMN IF NOT EXISTS media_slug TEXT;
ALTER TABLE ewc_news_posts ALTER COLUMN game_slug DROP NOT NULL;

CREATE INDEX IF NOT EXISTS idx_ewc_news_posts_game_status
