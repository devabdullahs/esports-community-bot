-- PostgreSQL schema for the app-owned tables in the current SQLite database.
-- This intentionally preserves the app's current row shapes:
-- - *_json columns remain text
-- - datetime('now') values remain UTC-like text strings
-- - integer booleans remain integer flags
--
-- Better Auth tables are managed separately by Better Auth's PostgreSQL
-- migration command and are copied only after those tables exist.

CREATE TABLE IF NOT EXISTS tournaments (
  id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  source       TEXT NOT NULL CHECK (source IN ('pandascore','startgg','liquipedia')),
  external_id  TEXT NOT NULL,
  game         TEXT,
  name         TEXT,
  url          TEXT,
  guild_id     TEXT NOT NULL,
  added_by     TEXT,
  active       INTEGER NOT NULL DEFAULT 1,
  created_at   TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  UNIQUE (source, external_id, guild_id)
);

CREATE TABLE IF NOT EXISTS matches (
  id             BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  tournament_id  BIGINT NOT NULL REFERENCES tournaments(id) ON DELETE CASCADE,
  source         TEXT NOT NULL,
  external_id    TEXT NOT NULL,
  name           TEXT,
  team_a         TEXT,
  team_b         TEXT,
  logo_a         TEXT,
  logo_b         TEXT,
  score_a        INTEGER DEFAULT 0,
  score_b        INTEGER DEFAULT 0,
  status         TEXT NOT NULL DEFAULT 'scheduled'
                   CHECK (status IN ('scheduled','running','finished')),
  scheduled_at   BIGINT,
  last_polled_at TEXT,
  updated_at     TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  UNIQUE (source, external_id)
);

CREATE INDEX IF NOT EXISTS idx_matches_status ON matches(status);
CREATE INDEX IF NOT EXISTS idx_matches_tournament ON matches(tournament_id);

