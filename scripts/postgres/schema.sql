);

CREATE INDEX IF NOT EXISTS idx_ewc_season_predictions_season
  ON ewc_season_predictions(guild_id, season, score DESC);

CREATE TABLE IF NOT EXISTS ewc_profile_links (
  auth_user_id     TEXT NOT NULL,
