-- Community comments on news posts (one-level threads; soft delete). See the
-- SQLite schema in src/db/index.js for the threading/visibility notes.
CREATE TABLE IF NOT EXISTS post_comments (
  id                BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  post_id           BIGINT NOT NULL REFERENCES ewc_news_posts(id) ON DELETE CASCADE,
  parent_comment_id BIGINT REFERENCES post_comments(id) ON DELETE SET NULL,
  root_comment_id   BIGINT REFERENCES post_comments(id) ON DELETE SET NULL,
  auth_user_id      TEXT NOT NULL,
  discord_user_id   TEXT NOT NULL,
  author_name       TEXT NOT NULL DEFAULT '',
  author_avatar_url TEXT,
  body              TEXT NOT NULL,
  status            TEXT NOT NULL DEFAULT 'visible'
                    CHECK (status IN ('visible','pending','hidden','rejected','deleted')),
  flag_reason_json  TEXT,
  auto_approve_at   BIGINT,
  created_at        TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  updated_at        TEXT NOT NULL DEFAULT (to_char(now() AT TIME ZONE 'UTC', 'YYYY-MM-DD HH24:MI:SS')),
  edited_at         TEXT,
  deleted_at        TEXT,
  deleted_by        TEXT
);
ALTER TABLE post_comments ADD COLUMN IF NOT EXISTS author_avatar_url TEXT;
CREATE INDEX IF NOT EXISTS idx_post_comments_post ON post_comments(post_id, status, created_at);
CREATE INDEX IF NOT EXISTS idx_post_comments_root ON post_comments(root_comment_id, created_at);
CREATE INDEX IF NOT EXISTS idx_post_comments_autoapprove ON post_comments(status, auto_approve_at);

